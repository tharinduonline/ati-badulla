                                    <h2 class="rl_tabs-title nn_tabs-title"><a id="anchor-entry-profile" class="anchor"></a>Entry Profile</h2>
                                    <ol>
                                    <li>School levels with G.C.E (A/L) qualifications in Mathematics, <g class="gr_ gr_491 gr-alert gr_spell gr_inline_cards gr_run_anim ContextualSpelling ins-del" id="491" data-gr-id="491">Bio Science</g> or Commerce (Preferable) and Art streams.</li>
                                    <li>Ordinary passes for English and Mathematics at the G.C.E (O/L)</li>
                                    </ol>
                                    <br>
                                    <h3>The Selection Criteria:</h3>
                                    <p>Candidates are selected through the order of merit in their GCE (A/L) examination and through an aptitude/ IQ test.</p>
                                    <p>Course Fee (Part Time): Rs. 12,000.00 per Semester<br><br></p>
                                    <h3><strong>Exemptions from the other academic or Professional programs:</strong></h3>
                                    <ul>
                                    <li>B. Sc. in Software Engineering and Information Technology – Open University of Sri Lanka</li>
                                    <li><g class="gr_ gr_482 gr-alert gr_spell gr_inline_cards gr_run_anim ContextualSpelling multiReplace" id="482" data-gr-id="482">Two year</g> exemption from University of Moratuwa BIT program.</li>
                                    <li>B. Eng. in University of Vocational Technology</li>
                                    <li>Degree in City and Guilds</li>
                                    <li>2 Year exemptions for B. Sc. in Software Engineering – Birmingham City University of UK</li>
                                    <li>With HNDIT and 2 Years Experience you can register for <g class="gr_ gr_483 gr-alert gr_gramm gr_inline_cards gr_run_anim Grammar multiReplace" id="483" data-gr-id="483">a M.</g> Sc. degree.</li>
                                    </ul>
                                    <h3><br>Important Information for Beginners to Select the Program</h3>
                                    <ul>
                                    <li>After the completion of <g class="gr_ gr_487 gr-alert gr_spell gr_inline_cards gr_run_anim ContextualSpelling multiReplace" id="487" data-gr-id="487">2 year</g> academic <g class="gr_ gr_486 gr-alert gr_gramm gr_inline_cards gr_run_anim Punctuation only-ins replaceWithoutSep" id="486" data-gr-id="486">program</g> you will be placed at a software development organization to follow an industrial training under the NAITA supervision for a period of 6 months.</li>
                                    <li>Jobs are available in the present IT job market for those who have completed the program.</li>
                                    </ul>
                                    <ul>
                                    <li>Carrier development path with degree programs <g class="gr_ gr_489 gr-alert gr_gramm gr_inline_cards gr_run_anim Grammar multiReplace" id="489" data-gr-id="489">are</g> available in local and foreign Universities.</li>
