						<h2>Higher National Diploma in Accountancy (HNDA)</h2>
						<p>The HNDA programme is the first professional accounting course introduced in Sri Lanka. It was started in 1943 as the National Diploma in Accountancy with the aim of detecting and preventing frauds, errors and malpractices in Ceylon Tea Estates and Ceylon Railway. Then, it was converted to a Higher National Diploma in Accountancy (HNDA) in 1946. The HNDA programme is the first professional accounting course introduced in Sri Lanka. It has been gaining popularity over the time and in 1963, an evening course was also introduced in order to meet the market demand. The HNDA was considered as an alternative to a B.Com degree offered by a Sri Lankan university by the Public Administration Circular No.46/90 of 1990. The mandate for auditing enterprises excluding public sector organizations as certified auditors was granted to the HNDA holders in 1968. From 2001, HNDA programme is conducted by the Sri Lanka Institute of Advanced Technological Education (SLIATE) at the Advanced Technological Institute (ATIs).
<br><br>
                                After completing HNDA program, the HNDA holders can start their carrier as registered auditor and audit companies except public companies. The syllabus of the HNDA programme was recently revised by taking into the account of the syllabus of the Institute of Chartered Accountants of Sri Lanka and some other syllabuses. The HNDA programme is conducted over four years on full time and part time basis. SLIATE has been conducting this programme from its inception in 1965 with the mission of creating excellent higher national diploma holders who are competent enough and equipped with modern technology in the fields of accounting and finance in order to contribute for the sustainable development of the country.</p>
                        
                        
