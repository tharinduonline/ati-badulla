                                <h2 class="rl_tabs-title nn_tabs-title"><a id="anchor-entry-profile" class="anchor"></a>Entry Profile</h2>
                                <h3>Full Time (<strong>The first and second year lectures are conducted as full time during the&nbsp;&nbsp; weekdays, while the third and fourth year lectures are conducted during &nbsp; the weekends.</strong>) ( Duration : 4 years)</h3>
                                <p style="text-align: justify;"><br>Passes for all the subjects in one sitting at the G.C.E. (A/L) examination with credit pass for Economics and Accountancy..</p>
                                <p style="text-align: justify;">A credit pass in Mathematics at the G.C.E (O/L) is considered as an alternative for a credit pass in either&nbsp;Economics or Accountancy at the G.C.E (A/L) examination.</p>
                                <p><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;AND</strong></p>
                                <p>An ordinary pass in English Language at the G.C.E. (O/L) examination.</p>
                                <h3>Part Time (Part time program will be conducted during the weekends) (Duration : 4 years)</h3>
                                <p><strong>Those who have completed GCE (A/L) examination on or before 2012, Should have one of followings:</strong></p>
                                <ol style="list-style-type: lower-roman;">
                                <li style="text-align: justify;">Passes for all four subjects / three subjects in one sitting at the G.C.E. (A/L) Examination<br><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; OR</strong></li>
                                <li style="text-align: justify;">Any of the certificate courses given below conducted by the Department of Technical Education &amp; Training (DTET).<br><br>
                                <ol type="A">
                                <li>Completion of National Certificate in Accounting Technicians.</li>
                                <li>Completion of National Certificate in Business Studies.</li>
                                <li>Completion of National Certificate in Accounting.</li>
                                </ol>
                                <br><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; AND</strong> <br>
                                <p>Applicant should be employed in the relevant field in a government institution/public enterprise/recognized firm or self-employment (Entrepreneur).</p>
                                <p>Those who possess the following qualifications will be exempted from the 1st year and will be admitted to the 2nd year when satisfying the other entry requirements and availability of places.</p>
                                <br>
                                <ol style="list-style-type: lower-roman;">
                                <li>Completion of the Higher National Diploma in Management / Business Administration/ Business Finance conducted by the (SLIATE).</li>
                                <li>Completion of second year of the Higher National Diploma in Commerce conducted by the (SLIATE) or (DTET).</li>
                                <li>Completion of the National Diploma in Business Studies conducted by the (SLIATE) or (DTET).</li>
                                <li>University Degree in relevant field (Accountancy/ B.Com./ Marketing Management /Business Administration/ Business Finance/ Business /Human Recourse Management) recognized by the University Grand Commission (UGC).</li>
                                <li>Completion of Executive level or CAB I and CAB II or Licentiate I and II conducted by the Institute of Chartered Accountants of Sri Lanka (ICASL).</li>
                                <li>Completion of Part I and Part II of the Chartered Institute of Management Accountants (CIMA – UK).</li>
                                <li>Completion of the Association of Accounting Technicians (AAT).</li>
                                <li>Completion of the Associated Certified Management Accountants (ACMA) conducted by the Institute of Certified Management Accountants (CMA-Sri Lanka).</li>
                                </ol>
                                <br>
                                <p>Note: - HNDA - (Full Time) / (Part Time)</p>
                                <p>According to the Public Administration circular No. 46/90, the Higher National Diploma in Accountancy has been recognized to accept as an alternative qualification to a General Degree in Commerce (Ordinary pass), awarded by a recognized University for recruitment purposes.</p>
                                </li>
                                </ol>
                                
                                
